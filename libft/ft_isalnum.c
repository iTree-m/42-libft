#include "libft.h"

int		ft_isalnum(int c)
{
	unsigned int c1;

	c1 = (unsigned int)c;
	if ((c1 >= 65 && c1 <= 90) || (c1 >= 97 && c1 <= 122) ||
			(c1 >= 48 && c1 <= 57))
		return (1);
	return (0);
}
