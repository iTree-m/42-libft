#include "libft.h"

size_t	ft_is_prime(int nb)
{
	int i;

	i = nb / 2;
	if (nb <= 1)
		return (0);
	while (i >= 2)
	{
		if (nb % i != 0)
			i--;
		else
			return (0);
	}
	return (1);
}
